    public function view_answers($id){
        $poll = Poll::findOrFail($id);
        $answers = null;
        if($poll){
            $answers = Option::where('poll_id', $poll->id)->get();
        }
        return view('pages.polls.answers', ['answers' => $answers, 'id' => $id]);
    }

    public function add_answer(Request $request, $id){
        Option::create(['poll_id' => $id, 'option' => $request->answer]);
        $polls = Poll::orderBy('date_from')->get();
        return view('pages.polls.index', compact('polls', $polls));    
    }

